export const primeVueBoundaryPolicy = {
  appBootstrapImports: {
    'apps/web-demo/src/plugins/modules/primevue.ts': {
      allowedPackageImports: ['@ccd/vue-primevue-adapter', '@ccd/vue-ui'],
      rawPrimeVueImports: 'forbidden',
    },
    'apps/desktop/src/plugins/index.ts': {
      allowedPackageImports: ['@ccd/vue-primevue-adapter'],
      rawPrimeVueImports: 'forbidden',
    },
  },
  rawPrimeVueAppImportAllowlist: [],
  governedBuildResolverFiles: ['apps/web-demo/build/resolvers/primevue.ts'],
  generatedComponentRegistryFiles: ['apps/web-demo/src/types/components.d.ts'],
  vueUiInternalPath: 'packages/vue-ui/src/',
  primeVueAdapterPath: 'packages/vue-primevue-adapter/src/',
}

export const approvedPrimeVueAppImportFiles = new Set(
  primeVueBoundaryPolicy.rawPrimeVueAppImportAllowlist
)

export const isPrimeVueModule = specifier =>
  specifier === 'primevue' || specifier.startsWith('primevue/') || specifier.startsWith('@primevue/')

export const extractPrimeVueReferences = content => {
  const references = []
  const sideEffectImportPattern = /^\s*import\s*['"]([^'"]+)['"]/gm
  let sideEffectMatch
  while ((sideEffectMatch = sideEffectImportPattern.exec(content)) !== null) {
    const specifier = sideEffectMatch[1]
    if (isPrimeVueModule(specifier)) {
      references.push({ kind: 'side-effect-import', specifier })
    }
  }

  const staticImportExportPattern =
    /^\s*(import|export)\s+(?!['"])(?:type\s+)?[\s\S]*?\sfrom\s*['"]([^'"]+)['"]/gm
  let staticMatch
  while ((staticMatch = staticImportExportPattern.exec(content)) !== null) {
    const specifier = staticMatch[2]
    if (isPrimeVueModule(specifier)) {
      references.push({
        kind: staticMatch[1] === 'export' ? 'export-from' : 'import-from',
        specifier,
      })
    }
  }

  const dynamicImportPattern = /\bimport\s*\(\s*['"]([^'"]+)['"]\s*\)/g
  let dynamicMatch
  while ((dynamicMatch = dynamicImportPattern.exec(content)) !== null) {
    const specifier = dynamicMatch[1]
    if (isPrimeVueModule(specifier)) {
      references.push({ kind: 'import-call', specifier })
    }
  }

  return references
}

export const isPrimeVueBuildResolverBoundaryFile = relPath =>
  primeVueBoundaryPolicy.governedBuildResolverFiles.includes(relPath)

export const isPrimeVueGeneratedComponentRegistryFile = (relPath, content) =>
  primeVueBoundaryPolicy.generatedComponentRegistryFiles.includes(relPath) &&
  content.includes('Generated by unplugin-vue-components')

export const isVueUiInternalFile = relPath =>
  relPath.startsWith(primeVueBoundaryPolicy.vueUiInternalPath)

export const isPrimeVueAdapterFile = relPath =>
  relPath.startsWith(primeVueBoundaryPolicy.primeVueAdapterPath)

export function classifyPrimeVueBoundaryReference(relPath, content, reference) {
  if (isVueUiInternalFile(relPath)) {
    if (reference.kind === 'export-from') {
      return {
        allowed: false,
        ruleId: 'primevue-public-api-leak',
        message: `@ccd/vue-ui must not publicly re-export raw PrimeVue modules such as "${reference.specifier}"`,
      }
    }

    return { allowed: true, reason: 'vue-ui-internal' }
  }

  if (isPrimeVueAdapterFile(relPath)) return { allowed: true, reason: 'adapter' }
  if (isPrimeVueBuildResolverBoundaryFile(relPath)) {
    return { allowed: true, reason: 'build-resolver' }
  }
  if (isPrimeVueGeneratedComponentRegistryFile(relPath, content)) {
    return { allowed: true, reason: 'generated-component-registry' }
  }
  if (approvedPrimeVueAppImportFiles.has(relPath)) {
    return { allowed: true, reason: 'approved-app-import' }
  }

  return {
    allowed: false,
    ruleId: 'primevue-direct-import-boundary',
    message: `Direct PrimeVue module "${reference.specifier}" is allowed only inside @ccd/vue-ui internals, @ccd/vue-primevue-adapter, governed build boundaries, or an approved app entrypoint`,
  }
}
